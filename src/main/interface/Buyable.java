package interfaces;

/**
 * @file Buyable.java
 * @brief Интерфейс для товаров, которые можно купить
 * @author VinLab Team
 * @version 1.0
 * @date 2024
 */

/**
 * @interface Buyable
 * @brief Интерфейс определяющий поведение покупаемого товара
 *
 * Данный интерфейс предоставляет методы для работы с покупкой товаров
 * в системе Vinlab. Реализация этого интерфейса обязательна для всех
 * классов товаров, которые должны поддерживать операцию покупки.
 */
public interface Buyable {

    /**
     * @brief Выполняет покупку товара
     * @param quantity - количество товара для покупки
     * @return double - общая стоимость покупки
     *
     * Метод выполняет операцию покупки указанного количества товара.
     * Возвращает общую стоимость покупки с учетом скидок и налогов.
     *
     * @throws IllegalArgumentException если quantity <= 0
     */
    double buy(int quantity);

    /**
     * @brief Проверяет доступность товара для покупки
     * @param quantity - количество товара для проверки
     * @return boolean - true если товар доступен, false в противном случае
     *
     * Метод проверяет, доступно ли указанное количество товара
     * для покупки на складе.
     */
    boolean isAvailable(int quantity);
}